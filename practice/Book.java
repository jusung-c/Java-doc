package practice;

public class Book {
    private String title;   // field price
    private int price;

    // setter, getter - 프로퍼티(property)
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
